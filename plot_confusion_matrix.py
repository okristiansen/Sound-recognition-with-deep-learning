# Row-normalise to percentages
row_sums = cm.sum(axis=1, keepdims=True)
cm_pct = np.divide(cm * 100.0, row_sums, out=np.zeros_like(cm, dtype=float), where=row_sums != 0)

# ── Heatmap ────────────────────────────────────────────────────────────────
fig, ax = plt.subplots(figsize=(22, 20))
im = ax.imshow(cm_pct, cmap="Blues", vmin=0, vmax=100)

ax.set_xticks(range(50))
ax.set_yticks(range(50))
ax.set_xticklabels(class_names, rotation=90, fontsize=7)
ax.set_yticklabels(class_names, fontsize=7)
ax.set_xlabel("Predicted", fontsize=12)
ax.set_ylabel("True", fontsize=12)
ax.set_title("SimpleCNN Fold-5 — Normalised Confusion Matrix (%)", fontsize=14, pad=12)

# Annotate cells that are non-zero
for i in range(50):
    for j in range(50):
        val = cm_pct[i, j]
        if val > 0:
            color = "white" if val > 55 else "black"
            ax.text(j, i, f"{val:.0f}", ha="center", va="center", fontsize=5, color=color)

plt.colorbar(im, ax=ax, fraction=0.02, pad=0.01, label="% of true class")
plt.tight_layout()
plt.savefig("confusion_matrix_fold5.png", dpi=150)
print("Saved: confusion_matrix_fold5.png")

# ── Per-class accuracy table ───────────────────────────────────────────────
tp = np.diag(cm)
totals = cm.sum(axis=1)
acc_per_class = tp / totals * 100

print(f"\n{'#':>3}  {'Class':<14}  {'Correct':>7}  {'Total':>5}  {'Acc%':>6}  {'Main confusion'}")
print("-" * 65)
for i in range(50):
    # find biggest off-diagonal prediction
    row = cm[i].copy()
    row[i] = 0
    worst_j = int(np.argmax(row))
    worst_n = int(row[worst_j])
    conf_str = f"-> {class_names[worst_j]} ({worst_n})" if worst_n > 0 else "-"
    marker = " *" if acc_per_class[i] < 50 else ""
    print(f"{i:>3}  {class_names[i]:<14}  {int(tp[i]):>7}  {int(totals[i]):>5}  {acc_per_class[i]:>5.1f}%  {conf_str}{marker}")

overall = tp.sum() / cm.sum() * 100
print(f"\nOverall accuracy: {overall:.1f}%  ({int(tp.sum())}/{int(cm.sum())})")
